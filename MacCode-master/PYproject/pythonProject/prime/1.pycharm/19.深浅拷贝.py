import copy

b = [1, [2, 3]]  # 原列表

# 1. 直接赋值
a1 = b
# 2. 浅拷贝
a2 = copy.copy(b)
# 3. 深拷贝
a3 = copy.deepcopy(b)

# 修改外层元素（重新赋值）
b[0] = 100
# 修改内层可变元素
b[1].append(4)

print('b=', b, id(b), '\t')  # [100, [2, 3, 4]]

print('a1=', a1, id(a1), '\t')  # [100, [2, 3, 4]]   → 外层、内层都受影响（同一对象）
print('a2=', a2, id(a2), '\t')  # [1, [2, 3, 4]]     → 外层不受影响，内层受影响（浅拷贝）
print('a3=', a3, id(a3), '\t')  # [1, [2, 3]]        → 外层、内层都不受影响（深拷贝）
